export function getAthleteName(name) {
  if (athleteMap[name.toLowerCase()]) {
    return athleteMap[name.toLowerCase()].name;
  }

  return name;
}

export function getAthleteFirstName(name) {
  if (athleteMap[name.toLowerCase()]) {
    const splitted = athleteMap[name.toLowerCase()].name.split(' ');
    return splitted[0];
  }

  return name;
}

